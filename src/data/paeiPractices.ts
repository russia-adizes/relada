function getDominantLetters(type: string): string[] {
  return ['P', 'A', 'E', 'I'].filter((l) => type.includes(l))
}

export function getPractice(personalityType: string | null): TypePractice {
  if (!personalityType) return PRACTICES.P

  const dominant = getDominantLetters(personalityType)
  if (dominant.length === 0) return PRACTICES.P
  if (dominant.length === 1) return PRACTICES[dominant[0]] ?? PRACTICES.P

  const comboKey = dominant.slice(0, 2).join('')
  if (COMBO_PRACTICES[comboKey]) return COMBO_PRACTICES[comboKey]

  return PRACTICES[dominant[0]] ?? PRACTICES.P
}

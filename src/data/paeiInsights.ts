function getDominantLetters(type: string): string[] {
  const letters = ['P', 'A', 'E', 'I']
  return letters.filter((l) => type.includes(l))
}

export function getInsight(personalityType: string | null): TypeInsight {
  if (!personalityType) return INSIGHTS.P

  const dominant = getDominantLetters(personalityType)

  if (dominant.length === 0) return INSIGHTS.P
  if (dominant.length === 1) return INSIGHTS[dominant[0]] ?? INSIGHTS.P

  const comboKey = dominant.slice(0, 2).join('')
  if (COMBOS[comboKey]) return COMBOS[comboKey]

  return INSIGHTS[dominant[0]] ?? INSIGHTS.P
}
